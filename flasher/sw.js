var CACHE_NAME = 'cache-v23';
var urlsToCache = [
  '/css/serialisactive.css',
  '/css/serialnotactive.css',
  '/images/noSerial.png',
  '/images/notconnectedgif.gif',
  '/images/upload.gif',
  '/images/flashComplete.png',
  '/images/failed.png',
  '/images/android-chrome-192x192.png',
  '/images/android-chrome-512x512.png',
  '/js/serialactive.js',
  '/pages/serialnotactive.html',
  '/',
  '/index.html',
  '/firmwares/firmware.bin',
  '/firmwares/bootloader_dio_40m.bin',
  '/firmwares/partitions.bin',
  '/firmwares/boot_app0.bin',
  '/hashes/hashes.json',
];

self.addEventListener('install', function(event) {
  // Perform install steps
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(function(cache) {
        console.log('Opened cache');
        return cache.addAll(urlsToCache);
      })
  );
});

self.addEventListener('fetch', function(event) {
  event.respondWith(
    caches.match(event.request)
      .then(function(response) {
        // Cache hit - return response
        if (response) {
          return response;
        }

        return fetch(event.request).then(
          function(response) {
            // Check if we received a valid response
            if(!response || response.status !== 200 || response.type !== 'basic') {
              return response;
            }

            // IMPORTANT: Clone the response. A response is a stream
            // and because we want the browser to consume the response
            // as well as the cache consuming the response, we need
            // to clone it so we have two streams.
            var responseToCache = response.clone();

            caches.open(CACHE_NAME)
              .then(function(cache) {
                cache.put(event.request, responseToCache);
              });

            return response;
          }
        );
      })
    );
});

self.addEventListener('activate', function(event) {

  var cacheAllowlist = [CACHE_NAME];

  event.waitUntil(
    caches.keys().then(function(cacheNames) {
      return Promise.all(
        cacheNames.map(function(cacheName) {
          if (cacheAllowlist.indexOf(cacheName) === -1) {
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
});
